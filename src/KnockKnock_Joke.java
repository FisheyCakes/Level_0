import javax.swing.JOptionPane;

public class KnockKnock_Joke {
	public static void main(String[] args) {
		JOptionPane.showInputDialog("Why did Suzie fall off the swing?");
		JOptionPane.showMessageDialog(null, "Because she didn't have any arms.");
		JOptionPane.showInputDialog("Knock Knock");
		JOptionPane.showMessageDialog(null, "Not Suzie");
	}

}
